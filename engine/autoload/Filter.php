<?php 

/*
|--------------------------------------------------------------------------
| Simple data filters class
|--------------------------------------------------------------------------
*/
class Filter 
{
	/*
	|--------------------------------------------------------------------------
	| Ivalidate email
	|--------------------------------------------------------------------------
	*/
	public function email($param) {
		return filter_var($param, FILTER_VALIDATE_EMAIL);	
	}
	/*
	|--------------------------------------------------------------------------
	| Ivalidate numeric
	|--------------------------------------------------------------------------
	*/
    public function numeric($param) {
        return is_numeric($param);
    }

}